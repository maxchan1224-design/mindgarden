export function composeSystemPrompt(ctx: PromptContext, task: string): string {
  const persona = PERSONAS[ctx.personaId];
  const parts = [
    SAFETY_LAYER,
    INTENT_LAYER,
    coreRules(ctx),
    persona.style,
    task,
  ];
  if (ctx.voiceMode) parts.push(VOICE_MODIFIER);
  return parts.join('\n\n');
}
