export function checkinTask(input: {
  emotions?: { name: string; intensity: number }[];
  text: string;
}): string {
  const emotionLine = input.emotions?.length
    ? `情緒:${input.emotions.map(e => `${e.name}(${e.intensity}/10)`).join('、')}`
    : '(冇揀情緒,只有文字)';

  return `
【今次任務:回應對方啱啱寫低嘅嘢】
${emotionLine}
內容:「${input.text}」

先判斷佢想要咩(A被聽見 / B想諗清楚 / C想要答案),再回應。
長度跟需要而定 — 佢寫得深,你可以講多啲;佢淨係想抖一抖,兩三句就夠。

輸出:嚴格 JSON,唔好有其他文字或者 markdown:
{"text": "你嘅回應", "safety": false}
`.trim();
}

export function dialogueTask(turnCount: number, topic?: string): string {
  const topicLine = topic ? `\n對話主題:${topic}` : '';
  const summaryHint =
    turnCount >= 4
      ? `\n你哋已經傾咗 ${turnCount} 個回合。如果你覺得傾得差唔多,可以喺回應結尾輕輕問:「想唔想我幫你將頭先傾嘅嘢整理一次?」— 但如果佢仲有好多嘢想講,就唔好打斷。`
      : '';

  return `
【今次任務:延續對話】${topicLine}
完整對話歷史喺 messages 入面。

記住:
- 每次都真係接住佢頭先講嘅嘢,唔好跳題
- 佢問你嘢,你就答佢,唔好用反問迴避
- 佢淨係想講,你就聽同陪
- 沿住佢開嘅方向行,唔好帶去你想去嘅方向${summaryHint}

輸出:嚴格 JSON:
{"text": "你嘅回應", "offerSummary": ${turnCount >= 4}, "safety": false}
`.trim();
}

export function summaryTask(): string {
  return `
【今次任務:溫柔總結呢場對話】
對方應承咗俾你整理。你嘅總結有三部分:
1. 「你今日講咗…」— 用返對方自己嘅字眼覆述重點,唔好改寫成你嘅演繹
2. 「我聽到嘅係…」— 講出對話底下嘅情緒或者需要(如果唔肯定,用「好似」「可能」)
3. 「留一條問題俾聽日嘅你:…」— 一條開放式問題,唔使今日答

鐵律:唔可以加入對方冇講過嘅事實。唔好落結論,唔好俾評價。

輸出:嚴格 JSON:
{"text": "總結內容", "safety": false}
`.trim();
}

export function dailyReflectionTask(entriesJson: string): string {
  return `
【今次任務:為對方生成今日回顧】
以下係對方今日嘅所有記錄(JSON):
${entriesJson}

生成一份安靜嘅日結。規則:
- 全部內容必須有記錄支持,唔可以推測或者虛構
- summary:似一個朋友幫佢覆述今日,唔係報告
- emotionalPattern:描述今日情緒嘅形狀(例如「朝早緊,黃昏鬆返」),唔好落好壞判斷
- wins:一至三項,細嘢都算數
- challenges:最多兩項,陳述,唔好加「你應該」
- gentleAdvice:如果今日一切平穩,可以留空
- reflectionQuestion:一條留俾聽日嘅開放式問題
- quote:一句同今日主題有共鳴嘅說話。可以引用真實出處(必須真確),或者寫一句無名嘅話。
  嚴禁虛構名人語錄,嚴禁老套心靈雞湯。

輸出:嚴格 JSON:
{"summary": "", "emotionalPattern": "", "wins": [], "challenges": [], "gentleAdvice": "", "reflectionQuestion": "", "quote": "", "safety": false}
`.trim();
}

